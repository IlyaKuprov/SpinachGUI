function tableOut = units()
%UNITS Legacy SpinachGUI unit catalogue and conversion factors.
%   FactorToSI values match GUI/System Core/unit.h.  Magnetic-field units
%   intentionally preserve the historical convention used by the legacy
%   EasySpin exporter, where Gauss is the local base and mTesla has factor 10.

name = ["Unitless"; "Unknown"; "Gauss"; "mTesla"; "Bohr magneton"; "ppm"; ...
    "Joule"; "Hz"; "cm^-1"; "KHz"; "MHz"; "GHz"; "eV"; ...
    "Seconds"; "nsec"; "mi_sec"; "Metres"; "Milimetre"; ...
    "Micrometre"; "Nanometre"; "Angstroms"; "Bohr Radius"];

factorToSI = [1; 1; 1; 10; 1; 1; ...
    1; 6.626068e-34; 1.9863e-23; 6.626068e-31; 6.626068e-28; 6.626068e-25; 1.60217646e-19; ...
    1; 1.0e-9; 1.0e-6; 1; 1.0e-3; 1.0e-6; 1.0e-9; 1.0e-10; 52.9177e-12];

tableOut = table(name, factorToSI, 1 ./ factorToSI, ...
    'VariableNames', {'Name', 'FactorToSI', 'FactorFromSI'});
end
